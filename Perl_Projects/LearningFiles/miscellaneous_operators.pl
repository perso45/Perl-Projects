$my_string="I'll be back! ";
print ($my_string x 20); #Repeat String

@my_list=(1..10); 		 #Range Operator
print "\n@mylist\n";

$my_var=20;
print "Starting Value: $my_var\n";

print ++$my_var . "\n";  #Pre-Increment, Adds 1
print --$my_var . "\n";  #Pre-Decrement, Removes 1

print "\n" . $my_var++ . "\n"; #Adds 1
print "$my_var\n";		 #Post-Increment

print "\n" . $my_var-- . "\n"; #Removes 1
print "$my_var\n";		 #Post-Decrement